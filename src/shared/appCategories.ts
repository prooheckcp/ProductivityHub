export function lookupKnownCategory(appName: string | null | undefined): string | null {
  if (!appName) return null
  return KNOWN_APP_CATEGORIES[appName.trim().toLowerCase()] ?? null
}

export function isDevToolsApp(appName: string | null | undefined): boolean {
  return lookupKnownCategory(appName) === 'Developer Tools'
}
